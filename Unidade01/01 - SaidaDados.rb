# O método 'puts' é uma função embutida em Ruby que imprime o argumento fornecido na saída padrão (normalmente, seu terminal ou prompt de comando)
# Neste caso, a string "Olá, mundo!" é passada como argumento
# Após imprimir o argumento, 'puts' adiciona automaticamente uma quebra de linha
puts "Olá, mundo!"

# Aqui, 'puts' é usado novamente para imprimir a string "Bem-vindo ao Ruby!"
# Assim como antes, uma quebra de linha é adicionada automaticamente após a string
puts "Bem-vindo ao Ruby!"

# O método 'print' é semelhante ao método 'puts' pois também imprime o argumento fornecido na saída padrão
# No entanto, ao contrário de 'puts', 'print' não adiciona uma quebra de linha após a impressão
# Aqui, a string "Olá, " é impressa, mas sem uma quebra de linha no final, então a próxima string impressa continuará na mesma linha
print "Olá, "

# Aqui, 'print' é usado para imprimir a string "mundo!"
# Como 'print' não adiciona uma quebra de linha, esta string será impressa na mesma linha que a string "Olá, " anterior
print "mundo!"
